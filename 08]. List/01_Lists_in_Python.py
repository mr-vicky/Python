myList = ["Vivek", 1, 2, 3, 4, True]

print(myList)

# List is Mutable
myList[0] = 100
print(myList)

# We can access any element by using their INDEX
print(myList[5])

# append(): method to add new element in the List
myList.append("Vivek")
print(myList)

# List can store heterogeneous type data i.e. data of more than one  type