# & Operation
a = 10
b = 1
print("a & b :", a & b) # set's bit 1 if both bits are 1 otherwise ste's to 0
print("a | b :", a | b) # set's bit to 1 if one of the bit is 1 otherwise set's to 0
print("a ^ b :", a ^ b) # set's bit to 1 if both bits are different otherwise set's to 0
print("a << b :", a << b) # Left Shift Operator: (2**b)*a
print("a >> b :", a >> b) # Right shift Operator: a/(2**b)