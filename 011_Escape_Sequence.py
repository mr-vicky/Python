# Escape Sequences ------------->
# \n - New Line
# \r - Carriage Return
# \f or \x0c - Line Feed
# \t - Horizontal tab 
# \v or \x0b - Vertical Tab
# \b - Backspace
# \a - alert
# \o - Octal
# \x - HexaDecimal
# \u - Unicode


print("abc", end="") # to print in the same line
print("def")

# \n : 
print("abc\ndef")

# \r : 
print("abc\rdef")

# \f :
print("\fij", end="")

# \t : 
print("efg\tij")

# \v :
print(f"abc")

#\b : 
print("abc\b")

# \a : 
print("xyz\a")
